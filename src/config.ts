/**
 * 插件使用说明 - 显示在 Koishi 控制台配置页面
 */
export const usage = `
# 群组管理助手 (GroupHelper)

全方位的群组管理解决方案，提供成员管理、自动回复、娱乐互动等功能。

---

## 🛠️ 基础管理

- \`kick <@用户> [-b] [群号]\`
  > 踢出用户，\`-b\` 表示同时加入黑名单
- \`ban <@用户> {时长} [群号]\`
  > 禁言用户，时长支持表达式（如 \`10m\`, \`1h\`）
- \`unban <@用户> [群号]\`
  > 解除用户禁言
- \`stop <@用户>\`
  > 短期禁言用户
- \`ban-all\` / \`unban-all\`
  > 开启/解除全体禁言
- \`nickname <@用户> [昵称]\`
  > 设置用户昵称，留空则清除

## 👑 管理员设置

- \`admin <@用户>\`
  > 设置管理员权限
- \`unadmin <@用户>\`
  > 取消管理员权限

## 👮 角色权限管理

- \`gauth.list\`
  > 列出所有可用角色
- \`gauth.info <@用户>\`
  > 查看指定用户的角色
- \`gauth.add <@用户> <角色>\`
  > 给用户添加角色
- \`gauth.remove <@用户> <角色>\`
  > 移除用户的角色
- \`getauth <@用户>\`
  > 查询用户详细权限状态

## ⚠️ 警告系统

- \`warn <@用户> [次数]\`
  > 警告用户，默认增加 1 次
- \`warn.clear <@用户>\`
  > 清除用户所有警告
- \`warn.list\`
  > 查看本群警告记录

## 💬 消息管理

- \`essence *引用消息*\`
  > 精华消息管理 (\`-s\` 设置, \`-r\` 取消)
- \`delmsg *引用消息*\`
  > 撤回指定消息
- \`send <群号> [-s] *引用消息*\`
  > 跨群发送消息 (\`-s\` 隐藏发送者)

## 🎖️ 头衔管理

- \`title -s <文本>\`
  > 设置群头衔
- \`title -r\`
  > 移除群头衔
- \`title -u <@用户>\`
  > 为指定用户操作

## 📝 日志与统计

- \`listlog [数量]\`
  > 查看最近操作日志
- \`clearlog\`
  > 清理日志文件 (\`-d\` 保留天数, \`-a\` 清理所有)
- \`cmdlogs\`
  > 查看命令执行流水 (\`-u\` 用户, \`-c\` 命令, \`-f\` 失败)
- \`cmdstats\`
  > 查看命令统计报表 (\`-r\` 排序, \`--sort\` 方式)

## 🔍 防撤回功能

- \`antirecall <@用户>\`
  > 查询用户撤回记录
- \`antirecall-config -e true\`
  > 开启本群防撤回
- \`antirecall.status\`
  > 查看防撤回统计

## 🔑 关键词管理

- \`verify\`
  > **入群验证** (\`-a\` 添加, \`-r\` 移除, \`-n\` 自动拒绝)
- \`forbidden\`
  > **违禁词** (\`-a\` 添加, \`-d\` 撤回, \`-b\` 禁言)
- \`keyword.add <词> <回复>\`
  > 添加自动回复

## 👋 欢迎与防复读

- \`welcome -s <消息>\`
  > 设置入群欢迎语 (支持 \`{at}\`, \`{user}\` 变量)
- \`antirepeat <阈值>\`
  > 设置防复读阈值 (0 关闭)

## 📢 订阅系统

- \`sub [类型]\`
  > 管理订阅通知 (类型: \`member\`, \`mute\`, \`warning\`, \`all\`)

## 🤖 AI 助手

- \`ai <内容>\`
  > 与 AI 对话 (\`-r\` 重置上下文)
- \`translate <文本>\`
  > AI 翻译 (简写 \`tsl\`)
- \`ai-config\`
  > 配置 AI 功能开关和提示词

## 🎲 娱乐功能

- \`dice [面数]\`
  > 掷骰子
- \`banme\`
  > **自助禁言抽卡** (赢了没事，输了禁言，有保底机制)

## 🧩 其他功能

- \`quit-group\`
  > 让 Bot 退出当前群
- \`ban-list\`
  > 查看本群禁言名单
- \`unban-random [数量]\`
  > 随机解除禁言（大赦天下）

---

### ⏱️ 时间表达式说明
支持 \`10s\`, \`5m\`, \`1h\`, \`1d\` 等格式，支持 \`+\`, \`*\` 等运算。
`